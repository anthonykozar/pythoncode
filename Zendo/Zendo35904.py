def makehistogram(arr):
    hist = {}
    for i in arr:
        if hist.has_key(i):
            hist[i] = hist[i] + 1
        else:
            hist[i] = 1
    return hist

def countrepeats(histogram):
    count = 0
    for key in histogram:
        if histogram[key] > 1:
            count += 1
    return count

def has3repeats(word):
    return countrepeats(makehistogram(list(word))) > 2

def exactly2repeats(word):
    return countrepeats(makehistogram(list(word))) == 2


